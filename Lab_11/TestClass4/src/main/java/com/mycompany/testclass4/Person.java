
package com.mycompany.testclass4;


public class Person 
{
   private String name;
    private int id;

    // Constructor
    public Person(String name, int id) 
    {
        this.name = name;
        this.id = id;
    }

    // Getters and Setters
    public String getName()
    {
        return name;
    }

    public int getID()
    {
        return id;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public void setID(int id) 
    {
        this.id = id;
    }
}
    

